primary key (oid));

--Pass table
create table pass (
oid bigint generated by default as identity,
mandant varchar(20),
passnummer bigint not null,
typ varchar(30) not null,
kode varchar(50) not null,
groesse bigint not null,
behoerde varchar(50) not null,
primary key (oid));

--Sachbearbeiter table
create table sachbearbeiter (
oid bigint generated by default as identity,
mandant varchar(20),
telefon bigint not null,
fax bigint not null,
funktion varchar(50) not null,
organisationseinheit varchar(50) not null,
primary key (oid));

 
