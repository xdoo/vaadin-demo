--Buerger table
create table buerger (
oid bigint generated by default as identity,
mandant varchar(20),
vorname varchar(50) not null,
nachname varchar(50) not null,
geburtsdatum date not null,
augenfarbe varchar(30) not null,
alive boolean not null,
primary key (oid));

--Wohnung table
create table wohnung (
oid bigint generated by default as identity,
mandant varchar(20),
stock varchar(50) not null,
ausrichtung varchar(50) not null,
primary key (oid));

--Adresse table
create table adresse (
oid bigint generated by default as identity,
mandant varchar(20),
primary key (oid));

--AdresseExtern table
create table adresseextern (
oid bigint generated by default as identity,
